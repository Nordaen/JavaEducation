package Chapter_3;

public class SwitchDemo {
    public static void switchdemo()
    {
        int i;

        for(i=0; i<10; i++)
            switch(i){
                case 0:
                    System.out.println("i equal 0");
                    break;
                case 1:
                    System.out.println("i equal 1");
                    break;
                case 2:
                    System.out.println("i equal 2");
                    break;
                case 3:
                    System.out.println("i equal 3");
                    break;
                case 4:
                    System.out.println("i equal 4");
                    break;

            }
    }

}
