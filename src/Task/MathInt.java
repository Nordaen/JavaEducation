package Task;

public class MathInt {
    public static int sum(int a, int b)
    {
        return a+b;
    }
    public static int sub(int a, int b)
    {
        return a-b;
    }
    public static int div(int a,int b)
    {
        return a/b;
    }
    public static int mult(int a, int b)
    {
        return a*b;
    }
    public static int rem(int a,int b)
    {
        return a % b;
    }
}
